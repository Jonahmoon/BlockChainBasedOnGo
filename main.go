// NewBlock create and return a Block
func NewBlock(transactions []*Transaction, prevBlockHash []byte) *Block {
	block := &Block{time.Now().Unix(), transactions, prevBlockHash, []byte{}, 0}
	pow := NewProofOfWork(block)
	nonce, hash := pow.StartMining() //挖矿，返回工作过程参数和区块的hash

	block.Hash = hash[:]
	block.Nonce = nonce

	return block
}


// NewGenesisBlock creates and returns genesis Block
func NewGenesisBlock(coinbase *Transaction) *Block {
	return NewBlock([]*Transaction{coinbase}, []byte{})
}

// CreateBlockchain 创建一个新的区块链数据库
func CreateBlockchain(address string) *Blockchain {

	if dbExists() {//查看数据库是否存在
		fmt.Println("Blockchain already exists.")
		os.Exit(1)
	}
	var tip []byte//tip存放最新区块的hash

	cbtx := NewCoinbaseTX(address, genesisCoinbaseData)//address收费
	genesis := NewGenesisBlock(cbtx)//新的创始区块，既把收费交易放入一个新的区块中，为第一笔交易

	db, err := bolt.Open(dbFile, 0600, nil)//打开数据库
	if err != nil {
		log.Panic(err)
	}

	err = db.Update(func(tx *bolt.Tx) error {//更新数据库事务
		b, err := tx.CreateBucket([]byte(blocksBucket))//创建一个数据库bucket
		if err != nil {
			log.Panic(err)
		}

		err = b.Put(genesis.Hash, genesis.Serialize())//把创世块和他的序列化放进去
		if err != nil {
			log.Panic(err)
		}

		err = b.Put([]byte("l"), genesis.Hash)//“l”指向创世块的哈希
		if err != nil {
			log.Panic(err)
		}
		tip = genesis.Hash//将最近块的哈希存入tip

		return nil
	})
	if err != nil {
		log.Panic(err)
	}

	bc := Blockchain{tip, db}//创建区块链结构

	bc.Reindex()

	return &bc
}

// txHash returns the hash of the Transaction
func (tx *Transaction) txHash() []byte {
	var hash [32]byte

	txCopy := *tx
	txCopy.ID = []byte{}

	hash = sha256.Sum256(txCopy.Serialize())

	return hash[:]
}

